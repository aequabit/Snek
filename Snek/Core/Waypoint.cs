namespace Snek.Core
{
    public struct Waypoint
    {
        /// <summary>
        ///     New direction of the entity.
        /// </summary>
        public Direction Direction;

        /// <summary>
        ///     X coordinate of the waypoint.
        /// </summary>
        public int X;

        /// <summary>
        ///     Y coordinate of the waypoint.
        /// </summary>
        public int Y;
    }
}